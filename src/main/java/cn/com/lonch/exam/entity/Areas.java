package cn.com.lonch.exam.entity;

/**
 * 区域表
 * @author fei.chang
 */
public class Areas {

    /**
     * 主键
     */
    private int id;

    /**
     * 区域名称
     */
    private String areasName;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAreasName() {
        return areasName;
    }

    public void setAreasName(String areasName) {
        this.areasName = areasName;
    }
}
